package com.ashish.billingapp;

public class Login {

	private String check;
	private String accountlock;
	private String forchgpwd;
	private String access;
	
	
	
	public String getCheck() {
		return check;
	}
	public void setCheck(String check) {
		this.check = check;
	}
	
	public String getAccountlock() {
		return accountlock;
	}
	public void setAccountlock(String accountlock) {
		this.accountlock = accountlock;
	}
	public String getAccess() {
		return access;
	}
	public void setAccess(String access) {
		this.access = access;
	}
	public String getForchgpwd() {
		return forchgpwd;
	}
	public void setForchgpwd(String forchgpwd) {
		this.forchgpwd = forchgpwd;
	}

}
